package oop4;

public class rectangle {


	
	public void findH(int ya, int yb) {
		System.out.println("Height : "+(yb-ya));
	}
	public void findW(int xc, int xb) {
		System.out.println("Width : "+ (xc-xb));
	}
	public void Area(int yb, int ya, int xc, int xb) {
		System.out.println("Area : "+((yb-ya)*(xc-xb)));
	}
	
	
}
